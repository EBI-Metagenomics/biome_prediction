USE emg;
SELECT
  STUDY_NAME,
  STUDY_ABSTRACT,
  SAMPLE_DESC,
  SAMPLE_NAME,
  SAMPLE_ALIAS,
  LINEAGE
FROM
  STUDY
  INNER JOIN
  STUDY_SAMPLE ON STUDY.STUDY_ID = STUDY_SAMPLE.STUDY_ID
  INNER JOIN
  SAMPLE ON STUDY_SAMPLE.SAMPLE_ID = SAMPLE.SAMPLE_ID
  INNER JOIN
  BIOME_HIERARCHY_TREE ON SAMPLE.BIOME_ID = BIOME_HIERARCHY_TREE.BIOME_ID;
